def check_data_imbalance(df, target_var):
    missing_vals = df.isnull().sum()
    if missing_vals.sum() > 0:
        st.warning(f"âš ï¸ Data has {missing_vals.sum()} missing values. Consider imputation.")
        if st.button("Show missing values details"):
            st.write(missing_vals[missing_vals > 0])

    constant_cols = [col for col in df.columns if df[col].nunique() <= 1]
    if constant_cols:
        st.warning(f"âš ï¸ Constant columns detected: {constant_cols}")

    Q1 = df[target_var].quantile(0.25)
    Q3 = df[target_var].quantile(0.75)
    IQR = Q3 - Q1
    outliers = ((df[target_var] < (Q1 - 1.5 * IQR)) | (df[target_var] > (Q3 + 1.5 * IQR))).sum()
    if outliers > 0:
        st.warning(f"âš ï¸ Potential outliers in target variable: {outliers} values")

